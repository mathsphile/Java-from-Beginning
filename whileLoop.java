public class whileLoop {
    public static void main(String args[])
    {
        int i=0;
        System.out.println("The odd numbers between 0 to 10 are ");
        while(i<10)
        {
            if(i%2!=0)
            {
                System.out.println(+i);

            }
            i++;
        }
    }
}
